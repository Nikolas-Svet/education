import numpy as np

# Ввод коэффициентов от пользователя
a11 = float(input("Введите коэффициент a11: "))
a12 = float(input("Введите коэффициент a12: "))
a21 = float(input("Введите коэффициент a21: "))
a22 = float(input("Введите коэффициент a22: "))

# Ввод правых частей уравнений
b1 = float(input("Введите правую часть b1: "))
b2 = float(input("Введите правую часть b2: "))

# Создаем матрицу коэффициентов и вектор правых частей
A = np.array([[a11, a12], [a21, a22]])
B = np.array([b1, b2])

# Проверка определителя матрицы
det_A = np.linalg.det(A)

if det_A == 0:
    print("Система не имеет единственного решения (либо не имеет решения, либо их бесконечно много).")
else:
    # Решение системы уравнений
    solution = np.linalg.solve(A, B)
    print("Решение системы: x =", solution[0], ", y =", solution[1])
